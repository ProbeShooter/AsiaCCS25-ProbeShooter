#include <stdio.h>
#include "aes.h"

static struct AES_ctx ctx;

int main()
{
    volatile uint8_t mk[16] = {0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xAA, 0xBB, 0xCC, 0xDD, 0xEE, 0xFF};
    volatile uint8_t buf[16] = {0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xAA, 0xBB, 0xCC, 0xDD, 0xEE, 0xFF} ;

    AES_init_ctx(&ctx, mk);

    while(1)
    {
        AES_ECB_encrypt(&ctx, buf);
    }
    return 0;
}
